LPTSTR GetResourceString(const int ids)
{
#define BUFFER_SIZE 10000

    HINSTANCE hInstance = GetModuleHandle(NULL);
    TCHAR buffer[BUFFER_SIZE];

    int len = LoadString(hInstance, ids, buffer, BUFFER_SIZE);

    LPTSTR result = (TCHAR*)malloc((len + 1) * sizeof(TCHAR));
    memcpy(result, buffer, len * sizeof(TCHAR));
    result[len] = 0;

    return result;

#undef BUFFER_SIZE
}

